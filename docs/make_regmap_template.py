"""
Generates docs/pssgen_regmap_template.xlsx
Run from the project root with the venv active:
    python docs/make_regmap_template.py
"""
import os
from openpyxl import Workbook
from openpyxl.styles import (
    Font, PatternFill, Alignment, Border, Side
)
from openpyxl.comments import Comment
from openpyxl.utils import get_column_letter
from openpyxl.worksheet.datavalidation import DataValidation

# ── Palette ──────────────────────────────────────────────────────────────────
DARK_BLUE   = "1F3864"
LIGHT_BLUE  = "D6E4F0"
LIGHT_YELLOW = "FFFACD"
WHITE       = "FFFFFF"
REQUIRED_HEADER_FILL = PatternFill("solid", fgColor=LIGHT_YELLOW)
STANDARD_HEADER_FILL = PatternFill("solid", fgColor=DARK_BLUE)
EXAMPLE_FILL         = PatternFill("solid", fgColor=LIGHT_BLUE)

def hdr_font(required=False):
    color = "000000" if required else WHITE
    return Font(name="Arial", bold=True, color=color, size=10)

def body_font(italic=False):
    return Font(name="Arial", italic=italic, size=10)

def make_header_cell(ws, row, col, value, required=False, comment_text=None):
    cell = ws.cell(row=row, column=col, value=value)
    cell.fill = REQUIRED_HEADER_FILL if required else STANDARD_HEADER_FILL
    cell.font  = hdr_font(required=required)
    cell.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
    if comment_text:
        c = Comment(comment_text, "pssgen")
        c.width  = 300
        c.height = 120
        cell.comment = c
    return cell

def set_example_row(ws, row_num, values):
    for col, val in enumerate(values, start=1):
        cell = ws.cell(row=row_num, column=col, value=val)
        cell.fill = EXAMPLE_FILL
        cell.font = body_font(italic=True)
        cell.alignment = Alignment(vertical="center", wrap_text=True)

def auto_width(ws, min_width=12):
    for col in ws.columns:
        max_len = min_width
        col_letter = get_column_letter(col[0].column)
        for cell in col:
            if cell.value:
                max_len = max(max_len, min(len(str(cell.value)), 40))
        ws.column_dimensions[col_letter].width = max_len + 2

# ── SHEET: Globals ────────────────────────────────────────────────────────────
def build_globals(wb):
    ws = wb.active
    ws.title = "Globals"

    # Cover note in A1
    ws["A1"] = (
        "pssgen Register Map Template v1.0\n"
        "Generated by pssgen — BelTech Systems LLC\n"
        "Fill in the Value column on this sheet, then\n"
        "populate the Blocks, RegisterMap, and Enums sheets.\n"
        "Delete the [EXAMPLE] rows before use."
    )
    ws["A1"].font = Font(name="Arial", bold=True, size=10)
    ws["A1"].alignment = Alignment(wrap_text=True, vertical="top")
    ws.row_dimensions[1].height = 72
    ws.merge_cells("A1:C1")

    # Header row at row 2
    headers = [
        ("Key",         "Configuration key name. Do not edit."),
        ("Value",       "Your project-specific value. Edit this column only."),
        ("Description", "Explains what this key controls. Do not edit."),
    ]
    for col, (hdr, cmt) in enumerate(headers, start=1):
        make_header_cell(ws, 2, col, hdr, required=False, comment_text=cmt)

    ws.freeze_panes = "A3"

    # Data rows
    rows = [
        ("project_name",    "MY_PROJECT",   "Project or IP block name"),
        ("revision",        "0.1",          "Semantic version of this register spec"),
        ("author",          "",             "Owner/author of this register map"),
        ("date",            "",             "Date of this revision (YYYY-MM-DD)"),
        ("data_width_bits", "32",           "Bus data width: 8, 16, 32, or 64"),
        ("endianness",      "Little",       "Little or Big"),
        ("bus_type",        "AXI4-Lite",    "AXI4-Lite, APB, Wishbone, or custom"),
        ("base_address",    "0x0000_0000",  "Base address of this IP block (hex)"),
        ("address_stride",  "0x04",         "Default register stride in bytes (hex)"),
    ]
    for r_idx, (key, val, desc) in enumerate(rows, start=3):
        ws.cell(row=r_idx, column=1, value=key).font  = body_font()
        ws.cell(row=r_idx, column=2, value=val).font  = body_font()
        ws.cell(row=r_idx, column=3, value=desc).font = body_font()

    auto_width(ws)
    ws.column_dimensions["B"].width = 20
    ws.column_dimensions["C"].width = 40

# ── SHEET: Blocks ─────────────────────────────────────────────────────────────
def build_blocks(wb):
    ws = wb.create_sheet("Blocks")

    block_headers = [
        ("block_name",
         "Name of the register block. Must be a valid HDL identifier.\n"
         "Matches block_name in the RegisterMap sheet."),
        ("base_address",
         "Block base address in hex. e.g. 0x4000_0000"),
        ("data_width_bits",
         "Bus data width for this block: 8, 16, 32, or 64"),
        ("reset_domain",
         "Name of the reset domain signal. e.g. RST_N, periph_rst"),
        ("clock_domain",
         "Name of the clock domain signal. e.g. CLK, periph_clk"),
        ("description",
         "Plain English description of what this block does."),
    ]
    for col, (hdr, cmt) in enumerate(block_headers, start=1):
        make_header_cell(ws, 1, col, hdr, comment_text=cmt)

    ws.freeze_panes = "A2"

    example = ["MY_BLOCK", "0x0000_0000", "32", "PERIPH_RST", "periph_clk",
               "My peripheral block"]
    example[0] = "[EXAMPLE — delete this row]  " + example[0]
    set_example_row(ws, 2, example)

    auto_width(ws)

