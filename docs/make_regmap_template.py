def build_registermap(wb):
    ws = wb.create_sheet("RegisterMap")

    for col, (hdr, required, cmt) in enumerate(REGMAP_COLS, start=1):
        make_header_cell(ws, 1, col, hdr, required=required, comment_text=cmt)

    ws.freeze_panes = "A2"

    # Example row 1 — RW control field
    ex1 = [
        "MY_BLOCK", "CTRL", "Control Register", "0x00", "32",
        "ENABLE", "0", "1", "RW", "0x0", "NO", "NA", "RW",
        "normal", "", "YES", "FUNC-REQ-201", "ctrl_enable",
        "dut.my_block.ctrl.enable", "Enable the peripheral core",
    ]
    ex1[0] = "[EXAMPLE — delete this row]  " + ex1[0]
    set_example_row(ws, 2, ex1)

    # Example row 2 — RO status field
    ex2 = [
        "MY_BLOCK", "STATUS", "Status Register", "0x04", "32",
        "TX_READY", "0", "1", "RO", "0x0", "YES", "set-on-1", "RO",
        "status", "", "YES", "FUNC-REQ-202", "",
        "", "Transmit ready — set by hardware when TX buffer empty",
    ]
    ex2[0] = "[EXAMPLE — delete this row]  " + ex2[0]
    set_example_row(ws, 3, ex2)

    # ── Data validation (via hidden ListsHelper sheet) ────────────────────────
    # Map column index (1-based) to validation list name
    VALIDATIONS = {
        9:  "access_list",
        11: "volatile_list",
        12: "hw_access_list",
        13: "sw_access_list",
        14: "field_kind_list",
        16: "uvm_cov_list",
    }
    for col_idx, list_name in VALIDATIONS.items():
        col_letter = get_column_letter(col_idx)
        dv = DataValidation(
            type="list",
            formula1=f"ListsHelper!${col_letter}$2:${col_letter}$20",
            allow_blank=True,
            showErrorMessage=True,
            errorTitle="Invalid value",
            error="Select a value from the dropdown list.",
        )
        dv.sqref = f"{col_letter}2:{col_letter}1048576"
        ws.add_data_validation(dv)

    auto_width(ws)

# ── SHEET: Enums ──────────────────────────────────────────────────────────────
def build_enums(wb):
    ws = wb.create_sheet("Enums")

    enum_headers = [
        ("enum_name",
         "Enum type name. Referenced by enum_ref in RegisterMap.\n"
         "e.g. spi_mode_t\n"
         "Repeat this name on every row that belongs to the same enum."),
        ("value",
         "Integer value for this enum symbol. e.g. 0, 1, 2, 3"),
        ("symbol",
         "Symbolic name. e.g. MODE0, DISABLED, ENABLED"),
        ("description",
         "What this value means in the context of the field.\n"
         "e.g. CPOL=0, CPHA=0"),
    ]
    for col, (hdr, cmt) in enumerate(enum_headers, start=1):
        make_header_cell(ws, 1, col, hdr, comment_text=cmt)

    ws.freeze_panes = "A2"

    examples = [
        ("example_mode_t", "0", "DISABLED", "Feature disabled"),
        ("example_mode_t", "1", "ENABLED",  "Feature enabled"),
        ("example_mode_t", "2", "BYPASS",   "Bypass mode active"),
    ]
    for r_idx, row in enumerate(examples, start=2):
        vals = list(row)
        if r_idx == 2:
            vals[0] = "[EXAMPLE — delete this row]  " + vals[0]
        set_example_row(ws, r_idx, vals)

    auto_width(ws)

# ── SHEET: ListsHelper (hidden) ───────────────────────────────────────────────
# Each validation list goes in its own column, header in row 1, values from row 2.
# Column positions must match the col_idx → list_name mapping in build_registermap.
# Col 9 = I, 11 = K, 12 = L, 13 = M, 14 = N, 16 = P

LISTS = {
    9:  ("access",          ["RW", "RO", "WO", "W1C", "W1S", "RC", "RS", "NA"]),
    11: ("volatile",        ["YES", "NO"]),
    12: ("hw_access",       ["NA", "RC", "W1C", "set-on-1", "set-on-read",
                             "write-only", "set-by-input", "status/clear",
                             "write-1-to-clear", "pulse", "TOG"]),
    13: ("sw_access",       ["RW", "RO", "WO", "RC", "RS", "NA"]),
    14: ("field_kind",      ["normal", "status", "reserved", "const", "alias"]),
    16: ("uvm_has_coverage",["YES", "NO"]),
}

def build_lists_helper(wb):
    ws = wb.create_sheet("ListsHelper")

    for col_idx, (label, values) in LISTS.items():
        col_letter = get_column_letter(col_idx)
        ws[f"{col_letter}1"] = label
        ws[f"{col_letter}1"].font = Font(name="Arial", bold=True, size=9)
        for r, v in enumerate(values, start=2):
            ws[f"{col_letter}{r}"] = v
            ws[f"{col_letter}{r}"].font = Font(name="Arial", size=9)

    ws.sheet_state = "hidden"

# ── Assemble ──────────────────────────────────────────────────────────────────
def main():
    wb = Workbook()

    build_globals(wb)
    build_blocks(wb)
    build_registermap(wb)
    build_enums(wb)
    build_lists_helper(wb)

    out_path = os.path.join(
        os.path.dirname(__file__), "pssgen_regmap_template.xlsx"
    )
    wb.save(out_path)
    print(f"Saved: {out_path}")

if __name__ == "__main__":
    main()
